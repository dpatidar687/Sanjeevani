    private void showPatientData() {
        
        try
        {
            
            String userid=DoctorLogin.getUserid();            
            String docid=DoctorDao.getDocid(userid);
            //System.out.println(docid);
          ArrayList<PatientPojo> patientList=PatientDao.getPatientOf(docid);
          Object rows[] =new Object [3];
          DefaultTableModel dtm=(DefaultTableModel)jtPatientData.getModel();
          for(PatientPojo e: patientList){
              rows[0]=e.getP_id();
              rows[1]=e.getF_name()+" "+e.getS_name();
              rows[2]=e.getOpd();
              
              
              dtm.addRow(rows);
            }
          }
          catch(Exception e){
                  JOptionPane.showMessageDialog(null,"Error in Showing the data");
          }
          
        }
