private void showReceptionistData() {
        
        try
        {
          ArrayList<EmpPojo> emplist=EmpDao.getAllReceptionist();
          Object rows[] =new Object [4];
          DefaultTableModel dtm=(DefaultTableModel)jtEmpData.getModel();
          for(EmpPojo e:emplist){
              rows[0]=e.getEmpid();
              rows[1]=e.getEmpname();
              rows[2]=e.getJob();
              rows[3]=e.getSal();
              dtm.addRow(rows);
            }
          }
          catch(Exception e){
                  JOptionPane.showMessageDialog(null,"Error in Showing the data");
          }
          
        }
