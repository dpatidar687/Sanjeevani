    public boolean validateInput(){
            String s=txtEmpName.getText();
            String r=txtSal.getText();
            //double r1=Double.parseDouble(r);
           // System.out.println(s.isEmpty());
           // System.out.println(r.isEmpty());
            if(s.isEmpty()||r.isEmpty()){
                return false;
            }
            else
                return true;
         
        
    }
    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
                if(validateInput()==false){
                JOptionPane.showMessageDialog(null,"Please fill all the fields ","Empty values",JOptionPane.ERROR_MESSAGE);
                return ;
            }
            try{
                String id=jcbEmpId.getSelectedItem().toString();
                String s=txtEmpName.getText();
                String r=txtSal.getText();
                double r1=Double.parseDouble(r);
               // String job=jcbJob.getSelectedItem().toString();
               EmpPojo e=new EmpPojo();
               e.setEmpid(id);
               e.setEmpname(s);
               e.setJob("");
               e.setSal(r1);
                boolean result=EmpDao.updateEmp(e);
                if(result){
                    JOptionPane.showMessageDialog(null,"Record successfully added to the database","Success!",JOptionPane.INFORMATION_MESSAGE);
                    clearText();
                    loadEmpDetails();
                }
                else
                JOptionPane.showMessageDialog(null,"could not change receptionist password to the database","Empty Values",JOptionPane.ERROR_MESSAGE);
            }
            catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"SQL ERROR ","Empty Values",JOptionPane.ERROR_MESSAGE);
                    ex.printStackTrace();
            }
                                            
                                                     

    }//GEN-LAST:event_btnUpdateActionPerformed

