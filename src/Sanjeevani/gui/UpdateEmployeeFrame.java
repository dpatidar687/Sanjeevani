    public void loadEmpDetails()
        {
            //jcbJob.setSelectedItem("Doctor");
            
            try{
                employee=EmpDao.getEmpList();
                //System.out.println("hiiiii");
                if(employee.size()==0){
                    JOptionPane.showMessageDialog(null,"No employees present");
                    btnUpdate.setEnabled(false);
                    return ;
                }
                btnUpdate.setEnabled(true);
                Set keys = employee.keySet();
                Iterator <String> it=keys.iterator();
                jcbEmpId.removeAllItems();
                while(it.hasNext())
                {
                    jcbEmpId.addItem(it.next());
                }
            } 
            
            catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"SQL ERROR in load Employees");
                ex.printStackTrace();
            }
        }
